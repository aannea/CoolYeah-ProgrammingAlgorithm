#include <iostream>

using namespace std;

int main()
{
    int c; //memasukkan variable c kedalam integer
    c=0;//menetapkan nilai awal dari variable c
    while (c<5){//perulangan menggunakan while
        cout << "Saya sangat menyukai C++"<<endl;//tampilan
        c++;//penambahan nilai c, agar perulangan mencapai target
    }
    return 0;
}
