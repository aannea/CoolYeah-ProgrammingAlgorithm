#include <iostream>

using namespace std;

int main()
{
    int c=0;//meamasukkan nilai c kedalam integer dan menetapkan nilainya

    do{ //periulangan menggunakan do, dan menampilkan dulu baru ditetapkan
            //berapa nilai diulangnya bepara kali
        cout<<"saya sangat menyukai C++"<<endl;//tampilan
        c++;//pertambahaan nilai c
    } while (c<5);//mengidentifiikasi nilai perulangan berapa kali
    return 0;
}
